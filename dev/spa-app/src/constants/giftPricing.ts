// デフォルト価格は0（ポイントはポーリングで同期）
export const DEFAULT_MESSAGE_PRICE = 0;

/**
 * ギフト（スタンプ）に対応する消費ポイントを取得する
 * 定義がなければメッセージ送信のデフォルト価格を返す
 */
export const getGiftPrice = (_categoryId: string, code: string): number => {
  return GIFT_PRICE_BY_CODE[code] ?? DEFAULT_MESSAGE_PRICE;
};
